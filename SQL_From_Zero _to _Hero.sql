Q54.Window Function 

SELECT age,department,
SUM(age)  OVER( PARTITION BY department ) AS "Total",
AVG(age)  OVER( PARTITION BY department ) AS "Average",
COUNT(age)  OVER( PARTITION BY department ) AS "Count",
MIN(age)  OVER( PARTITION BY department ) AS "Min",
MAX(age)  OVER( PARTITION BY department) AS "Max"
FROM toolkits



SELECT age,
ROW_NUMBER()OVER(ORDER BY age) AS "ROW_NUMBER",
RANK() OVER(ORDER BY age) AS "RANK",
DENSE_RANK()OVER(ORDER BY age) AS "DENSE_RANK",
PERCENT_RANK() OVER(ORDER BY age) AS "PERCENT_RANK"
FROM toolkits



SELECT id,
FIRST_VALUE(id)  OVER( ORDER BY id) AS "FIRST_VALUE",
LAST_VALUE(id)  OVER( ORDER BY id) AS "LAST_VALUE",  
LEAD(id)  OVER( ORDER BY id) AS "LEAD",
LAG(id)  OVER( ORDER BY id) AS "LAG"
FROM toolkits

SELECT id,
